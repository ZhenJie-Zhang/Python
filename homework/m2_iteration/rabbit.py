# 7.	迴圏的練習-rabbit
# 老王養了一群兔子，若三隻三隻一數，剩餘一隻；若五隻五隻一數，剩餘三隻；若七隻七隻一數，剩餘二隻。試問兔子最少有幾隻。
print('老王養了一群兔子，\n若三隻三隻一數，剩餘一隻；\n若五隻五隻一數，剩餘三隻；\n若七隻七隻一數，剩餘二隻。\n試問兔子最少有幾隻。')
rabbit = 0
while True:
    rabbit += 1
    if rabbit % 3 == 1 and rabbit % 5 == 3 and rabbit % 7 == 2:
        break
print('----------------------------------')
print('老王最少養了{}隻兔子'.format(rabbit))